public class Dow {
    public static void main(String[] args) {
        int Dow = 3;
        switch (Dow) {
            case 1:
                System.out.println("Monday");
                break;
            case 2:
                System.out.println("Tuesday");
                break;
            case 3:
                System.out.println("Wednesday");
                break;
            default:
                System.out.println("Unknown day");
        }
    }
}
